package com.favstore.order.serviceapp.model;

public class OrderItemDetails {

	private String description;
	private String sku;
	private String productId;
	private String productDetails;
	private String catagoryId;
	private String catagoryType;	
	private Double quantity;
	private Double rate;
	private Double discount;
	private Double subtotal;
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	/**
	 * @return the sku
	 */
	public String getSku() {
		return sku;
	}
	/**
	 * @param sku the sku to set
	 */
	public void setSku(String sku) {
		this.sku = sku;
	}
	/**
	 * @return the productId
	 */
	public String getProductId() {
		return productId;
	}
	/**
	 * @param productId the productId to set
	 */
	public void setProductId(String productId) {
		this.productId = productId;
	}
	/**
	 * @return the productDetails
	 */
	public String getProductDetails() {
		return productDetails;
	}
	/**
	 * @param productDetails the productDetails to set
	 */
	public void setProductDetails(String productDetails) {
		this.productDetails = productDetails;
	}
	/**
	 * @return the catagoryId
	 */
	public String getCatagoryId() {
		return catagoryId;
	}
	/**
	 * @param catagoryId the catagoryId to set
	 */
	public void setCatagoryId(String catagoryId) {
		this.catagoryId = catagoryId;
	}
	/**
	 * @return the catagoryType
	 */
	public String getCatagoryType() {
		return catagoryType;
	}
	/**
	 * @param catagoryType the catagoryType to set
	 */
	public void setCatagoryType(String catagoryType) {
		this.catagoryType = catagoryType;
	}
	/**
	 * @return the quantity
	 */
	public Double getQuantity() {
		return quantity;
	}
	/**
	 * @param quantity the quantity to set
	 */
	public void setQuantity(Double quantity) {
		this.quantity = quantity;
	}
	/**
	 * @return the rate
	 */
	public Double getRate() {
		return rate;
	}
	/**
	 * @param rate the rate to set
	 */
	public void setRate(Double rate) {
		this.rate = rate;
	}
	/**
	 * @return the discount
	 */
	public Double getDiscount() {
		return discount;
	}
	/**
	 * @param discount the discount to set
	 */
	public void setDiscount(Double discount) {
		this.discount = discount;
	}
	/**
	 * @return the subtotal
	 */
	public Double getSubtotal() {
		return subtotal;
	}
	/**
	 * @param subtotal the subtotal to set
	 */
	public void setSubtotal(Double subtotal) {
		this.subtotal = subtotal;
	}
	
	
	
		
}
